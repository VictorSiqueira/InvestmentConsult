function convertJsonDataToChartData(json){
  var chartArray = [];
  json.reverse();//temporario para garantir que o mais recente dado seja o ultimo
  for(i in json){
    var array = [json[i].Date, json[i].Low, json[i].Open, json[i].Close, json[i].High]
    chartArray.push(array);
  }
  return chartArray;
}

